setcps(140/60/4)

texture: note("g#4 g#4 g#4 g#4")
  .s("gm_pad_choir")
  .add(note(perlin.range(-0.1, 0.1)))
  .lpf(sine.range(400, 2000).slow(8))
  .gain(0.4)
  .attack(0.5)
  .release(2)
  .pan(sine.range(0.2, 0.8).slow(4))

sub: note("g#2")
  .s("sine")
  .struct("<x ~ ~ ~ ~ ~ ~ ~>")
  .gain(0.6)
  .lpf(150)

_glitch: s("bd bd ~ bd, ~ cp ~ cp*2, hh*16")
  .bank("RolandTR909")
  .sometimes(ply(2))
  .sometimes(x => x.speed("-1"))
  .gain("0.9 0.8 0.7 0.6")

_perc: s("rim ~ rim rim*4")
  .bank("RolandTR808")
  .n("<0 1 2 3>")
  .sometimes(x => x.chop(8))
  .pan("0 1 0.5")
  .gain(0.8)

_hats: s("hh*8")
  .bank("RolandTR707")
  .n(run(4))
  .sometimes(x => x.speed("1.5"))
  .lpf("<2000 500 1000 3000>")
  .gain(0.6)

clicks: s("~ ~ ~ <bd cp rim>*<2 4 8>")
